			 <p>O Sporting Clube Campomaiorense nasceu a 1 de Julho de 1926, sendo o seu primeiro presidente Domingos António Rabiais. É desconhecido quando se tornou a 27ª Filial do Sporting Clube de Portugal, mas pela data de fundação sua e das filiais com nº próximo, é provável que tenha sido quase de imediato. Filiou-se na Associação de Futebol de Portalegre logo em 1926.
				 O SC Campomaiorense só começou a participar em provas nacionais na época de 1945/46, em que disputou a 2ª divisão nacional O Clube utiliza o Estádio Capitão César Correia, que é propriedade da Casa do Povo de Campo Maior. Na maioria das temporadas, o Clube jogou na 2ª e 3ª divisão, com algumas passagens pelas divisões distritais, tendo vencido a série D da 3ª Divisão Nacional em 1960/61.

				 No final dos anso 80 o clube foi apoiado pela família Nabeiro, ganhando o Campeonato Nacional da 2ª Divisão B em 1992/93, e ascendendo à 1ª divisão em 1995/96, e conseguindo, até abandonar o futebol profissional, 5 presenças na 1ª divisão, e ainda vencer a 2ª Divisão em 1996/97.


				 Campomaiorense na década de 20
				 Com a mudança do símbolo do clube proposta em 1997 e aceite em assembleia geral extraordinária no dia 2 de Maio de 1998, sendo a partir dessa data um galgo, a direcção do clube lidarada por João Manuel Nabeiro propunha-se reunir em torno desta ideia, toda uma região alentejana que, em termos desportivos, tinha o Campomaiorense como expoente máximo, com o lema “Campomaiorense, mais que um clube, uma região”.

				 Para além do novo símbolo, nos Estatutos do Clube ficou também consagrada a possibilidade de utilizar as cores amarelo torrado e grená, para além das tradicionais verde e branca. Hoje em dia, o Campomaiorense, é conhecido como o Clube do Galgo, apesar de ser ainda uma Filial do Sporting Clube de Portugal.

				 Campomaiorense em 1935
				 No final da temporada 2001/02, com o declínio desportivo verificado, o SC Campomaiorense extinguiu a equipa de futebol profissional. Manteve as camadas jovens, e a partir de 2006/07 voltou a ter uma equipa sénior amadora a competir nas divisões distritais de Portalegre.
